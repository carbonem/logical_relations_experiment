    + exists (B' ∥ psubst shift P); split; [exact: PB_ParL HB | apply: CGC_one; exact: CG_PComm].
  - (* CG_PAssoc *)
    move=> n P Q R c r P' HT.
    case: (ltsbP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltsbP_parE HA) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists (A2 ∥ (psubst shift Q ∥ psubst shift R)); split;
          [exact: PB_ParL HA2 | apply: CGC_one; exact: CG_PAssoc].
      * exists (psubst shift P ∥ (B2 ∥ psubst shift R)); split;
          [apply: PB_ParR; exact: PB_ParL HB2 | apply: CGC_one; exact: CG_PAssoc].
    + exists (psubst shift P ∥ (psubst shift Q ∥ B')); split;
        [apply: PB_ParR; exact: PB_ParR HB | apply: CGC_one; exact: CG_PAssoc].
  - (* CG_PAssoc' *)
    move=> n P Q R c r P' HT.
    case: (ltsbP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists ((A' ∥ psubst shift Q) ∥ psubst shift R); split;
        [apply: PB_ParL; exact: PB_ParL HA | apply: CGC_one; exact: CG_PAssoc'].
    + case: (ltsbP_parE HB) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists ((psubst shift P ∥ A2) ∥ psubst shift R); split;
          [apply: PB_ParL; exact: PB_ParR HA2 | apply: CGC_one; exact: CG_PAssoc'].
      * exists ((psubst shift P ∥ psubst shift Q) ∥ B2); split;
          [exact: PB_ParR HB2 | apply: CGC_one; exact: CG_PAssoc'].
  - (* CG_PZero *)
    move=> n P c r P' HT.
    case: (ltsbP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists A'; split; [exact: HA | apply: CGC_one; exact: CG_PZero].
    + by case: (pinv_b_end HB).
  - (* CG_PZero' *)
    move=> n P c r P' HT.
    exists (P' ∥ ∅); split; [exact: PB_ParL HT | apply: CGC_one; exact: CG_PZero'].
  - (* CG_NuZero *)
    move=> n c r P' HT.
    case: (ltsbP_resE HT) => [Hopen | [P'0 [_ Hres]]].
    + by case: (pinv_f_end Hopen).
    + by case: (pinv_b_end Hres).
  - (* CG_NuZero' *)
    move=> n c r P' HT. by case: (pinv_b_end HT).
  - (* CG_Extr *)
    move=> n P Q c r P' HT.
    case: (ltsbP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltsbP_resE HA) => [Hopen | [P1 [-> Hb]]].
      * (* subcase i *)
        exists (A' ∥ psubst shift Q); split.
        - apply: PB_Open. apply: PF_ParL. exact: Hopen.
        - exact: cong_refl.
      * (* subcase ii *)
        exists ((ν) (psubst (swap_ch zero one) (P1 ∥ psubst shift (psubst shift Q)))); split.
        - apply: PB_Res. apply: PB_ParL. exact: Hb.
        - rewrite /= psubst_swap01_shift2.
          apply: CGC_one. exact: (CG_Extr (psubst (swap_ch zero one) P1) (psubst shift Q)).
    + (* subcase iii *)
      exists ((ν) (psubst (swap_ch zero one) (psubst shift P ∥ psubst (up_ch shift) B'))); split.
      * apply: PB_Res. apply: PB_ParR. exact: (ltsbP_ren HB shift).
      * rewrite /= psubst_swap_shift psubst_swap_up_shift.
        apply: CGC_one. exact: (CG_Extr (psubst (up_ch shift) P) B').
  - (* CG_Extr' *)
    move=> n P Q c r P' HT.
    case: (ltsbP_resE HT) => [Hopen | [P'0 [-> Hres]]].
    + (* Open: free-send from P ∥ ⟨shift⟩Q *)
      case: (ltsfP_parE Hopen) => [[A' [-> Hf]]|[B' [-> HB]]].
      * (* from P *)
        exists (A' ∥ psubst shift Q); split.
        - apply: PB_ParL. apply: PB_Open. exact: Hf.
        - exact: cong_refl.
      * (* from ⟨shift⟩Q: vacuous *)
        case: (ltsfP_ren_inv HB erefl) => c0 [d0 [P0 [_ Ed _ _]]].
        move: Ed. case: d0 => d0n d0r. by case.
    + (* Res: bound-send from P ∥ ⟨shift⟩Q *)
      case: (ltsbP_parE Hres) => [[A2 [-> HA2]]|[B2 [-> Hb2]]].
      * (* PB_ParL from P *)
        exists ((ν) (psubst (swap_ch zero one) A2) ∥ psubst shift Q); split.
        - apply: PB_ParL. apply: PB_Res. exact: HA2.
        - rewrite /= psubst_swap01_shift2.
          apply: CGC_one. exact: (CG_Extr' (psubst (swap_ch zero one) A2) (psubst shift Q)).
      * (* PB_ParR from ⟨shift⟩Q *)
        case: (ltsbP_ren_inv Hb2 erefl) => c0 [P0 [Ec -> Hb0]].
        have Ecc := pren_shift_inj (esym Ec). subst c0.
        exists ((ν) (psubst (up_ch shift) P) ∥ P0); split.
        - apply: PB_ParR. exact: Hb0.
        - rewrite /= psubst_swap_shift psubst_swap_up_shift.
          apply: CGC_one. exact: (CG_Extr' (psubst (up_ch shift) P) P0).
  - (* CG_NuSwap *)
    move=> n P c r P' HT.
    case: (ltsbP_resE HT) => [Hopen | [P'0 [-> Hres]]].
    + (* PB_Open with PF_Res: subcase a *)
      case: (ltsfP_resE Hopen) => P'' [-> Hf].
      exists ((ν) (psubst (swap_ch zero one) (psubst (swap_ch zero one) P''))); split.
      * apply: PB_Res. apply: PB_Open.
        have HF := ltsfP_ren Hf (swap_ch zero one).
        rewrite pren_swap01_pshift2 in HF.
        exact: HF.
      * rewrite psubst_swap01_invol. exact: cong_refl.
    + (* PB_Res: subcases b, c *)
      case: (ltsbP_resE Hres) => [Hf | [P'1 [-> Hb]]].
      * (* subcase b: inner PB_Open *)
        exists ((ν) (psubst (swap_ch zero one) P'0)); split.
        - apply: PB_Open. apply: PF_Res.
          have HF := ltsfP_ren Hf (swap_ch zero one).
          rewrite pren_swap01_pshift2 in HF.
          exact: HF.
        - exact: cong_refl.
      * (* subcase c: inner PB_Res *)
        exists ((ν) (psubst (swap_ch zero one)
                      ((ν) (psubst (swap_ch zero one) (psubst (up_ch (swap_ch zero one)) P'1))))); split.
        - apply: PB_Res. apply: PB_Res.
          have HB := ltsbP_ren Hb (swap_ch zero one).
          rewrite pren_swap01_pshift2 in HB.
          exact: HB.
        - rewrite /=.
          apply: (CGC_trans (Q := (ν) (ν) (psubst (swap_ch zero one)
                    (psubst (up_ch (swap_ch zero one)) (psubst (swap_ch zero one) P'1))))).
          + apply: CGC_one.
            exact: (CG_NuSwap (psubst (up_ch (swap_ch zero one)) (psubst (swap_ch zero one) P'1))).
          + rewrite psubst_braid. exact: cong_refl.
  - (* CG_Zero *)
    move=> n c r P' HT. by case: (pinv_b_end HT).
  - (* CG_ParL *)
    move=> n P0 P0' Q Hc IH c r P' HT.
    case: (ltsbP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (IH c r A' HA) => A2 [HA2 HcA].
      exists (A2 ∥ psubst shift Q); split;
        [exact: PB_ParL HA2 | exact: cong_parL (psubst shift Q) HcA].
    + exists (psubst shift P0' ∥ B'); split.
      * exact: PB_ParR HB.
      * apply: cong_parL. apply: CGC_one. exact: (cong1_ren Hc shift).
  - (* CG_Res *)
    move=> n P0 P0' Hc IH c r P' HT.
    case: (ltsbP_resE HT) => [Hopen | [P'0 [-> Hres]]].
    + case: (cong1_resp_ltsf Hc Hopen) => Q'' [HQ HcQ].
      exists Q''; split; [exact: PB_Open HQ | exact: HcQ].
    + case: (IH (pshift c) r P'0 Hres) => Q' [HQ HcQ].
      exists ((ν) (psubst (swap_ch zero one) Q')); split.
      * exact: PB_Res HQ.
      * apply: cong_nu. exact: (cong_ren HcQ (swap_ch zero one)).
  - (* CG_Wait *)
    move=> n c0 K K' Hc IH c r P' HT. by case: (pinv_b_wait HT).
  - (* CG_Close *)
    move=> n c0 K K' Hc IH c r P' HT. by case: (pinv_b_close HT).
  - (* CG_Ins *)
    move=> n c0 r0 K K' Hc IH c r P' HT. by case: (pinv_b_ins HT).
  - (* CG_Del *)
    move=> n c0 d K K' Hc IH c r P' HT. by case: (pinv_b_del HT).
  - (* CG_Sel *)
    move=> n c0 b K K' Hc IH c r P' HT. by case: (pinv_b_sel HT).
  - (* CG_Bra *)
    move=> n c0 K1 K1' K2 K2' Hc1 IH1 Hc2 IH2 c r P' HT. by case: (pinv_b_bra HT).
Qed.

Lemma cong_resp_ltsb n (P Q : procP n) :
  P ≡ Q -> forall (c : pch n) (r : pol) (P' : procP n.+1),
  ltsbP c r P P' -> exists Q', ltsbP c r Q Q' /\ P' ≡ Q'.
Proof.
  move=> H. elim: H => {n P Q}.
  - move=> n P Q Hc c r P' HT. exact: (cong1_resp_ltsb Hc HT).
  - move=> n P Q R _ IH1 _ IH2 c r P' HT.
    case: (IH1 c r P' HT) => Q1 [HQ1 Hc1].
    case: (IH2 c r Q1 HQ1) => R1 [HR1 Hc2].
    exists R1; split; [exact: HR1 | exact: CGC_trans Hc1 Hc2].
Qed.


