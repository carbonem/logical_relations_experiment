(** ** τ ⇒ red: the harmony lemma, forward direction

    [ltstP P Q -> red P Q].  A nested induction on the two
    synchronizing derivations, peeling one binder at a time: each
    [Res] case is one scope extrusion and one [shift], each frame case
    one parallel rearrangement.  No normal forms, no iterated-ν
    arithmetic. *)

(** *** Step 0: small identities *)

Lemma pflip_invol n (c : pch n) : pflip (pflip c) = c.
Proof. by case: c => x r; rewrite /pflip /= flipp_invol. Qed.

Lemma psubst_swap_shift n (Q : procP n.+1) :
  psubst (swap_ch zero one) (psubst shift Q) = psubst (up_ch shift) Q.
Proof.
  rewrite psubst_comp. apply: psubst_ext => -[z|] //=.
Qed.

Lemma pshift_pflip n (c : pch n) : pshift (pflip c) = pflip (pshift c).
Proof. exact: pren_flip. Qed.

(** *** Step 1: parallel rearrangement helpers

    Short [CGC_trans] chains over the commutative-monoid axioms; the
    two [red_cong] lemmas are one [R_Struc] each, [red_parR] mirrors
    [R_Par] through [CG_PComm]. *)

Lemma cong_pullL n (S Q1 Q2 : procP n) : (S ∥ (Q1 ∥ Q2)) ≡ ((S ∥ Q1) ∥ Q2).
Proof. apply: CGC_one. exact: CG_PAssoc'. Qed.

Lemma cong_pullR n (S Q1 Q2 : procP n) : (S ∥ (Q1 ∥ Q2)) ≡ ((S ∥ Q2) ∥ Q1).
Proof.
  apply: (CGC_trans (Q := S ∥ (Q2 ∥ Q1))).
  - apply: cong_parR. apply: CGC_one. exact: CG_PComm.
  - apply: CGC_one. exact: CG_PAssoc'.
Qed.

Lemma cong_exL n (P1 P2 Q : procP n) : ((P1 ∥ P2) ∥ Q) ≡ ((P1 ∥ Q) ∥ P2).
Proof.
  apply: (CGC_trans (Q := P1 ∥ (P2 ∥ Q))).
  - apply: CGC_one. exact: CG_PAssoc.
  - apply: (CGC_trans (Q := P1 ∥ (Q ∥ P2))).
    + apply: cong_parR. apply: CGC_one. exact: CG_PComm.
    + apply: CGC_one. exact: CG_PAssoc'.
Qed.

Lemma cong_exR n (P1 P2 Q : procP n) : ((P1 ∥ P2) ∥ Q) ≡ ((P2 ∥ Q) ∥ P1).
Proof.
  apply: (CGC_trans (Q := (P2 ∥ P1) ∥ Q)).
  - apply: cong_parL. apply: CGC_one. exact: CG_PComm.
  - apply: (CGC_trans (Q := P2 ∥ (P1 ∥ Q))).
    + apply: CGC_one. exact: CG_PAssoc.
    + apply: (CGC_trans (Q := P2 ∥ (Q ∥ P1))).
      * apply: cong_parR. apply: CGC_one. exact: CG_PComm.
      * apply: CGC_one. exact: CG_PAssoc'.
Qed.

Lemma red_congL n (P P2 Q : procP n) : P ≡ P2 -> red P2 Q -> red P Q.
Proof. move=> H Hr. exact: R_Struc H Hr (cong_refl Q). Qed.

Lemma red_congR n (P Q2 Q : procP n) : red P Q2 -> Q2 ≡ Q -> red P Q.
Proof. move=> Hr H. exact: R_Struc (cong_refl P) Hr H. Qed.

Lemma red_parR n (P Q Q' : procP n) : red Q Q' -> red (P ∥ Q) (P ∥ Q').
Proof.
  move=> H.
  apply: (R_Struc (P' := Q ∥ P) (Q' := Q' ∥ P)).
  - apply: CGC_one. exact: CG_PComm.
  - exact: R_Par.
  - apply: CGC_one. exact: CG_PComm.
Qed.

(** *** Step 2: base combination lemmas

    A fixed sender prefix against a receiver derivation.  Induction on
    the receiver (the continuation [K] quantified inside the motive so
    the [Res] case can shift it): [Pfx] fires the reduction axiom,
    each frame case is a [pull] rearrangement, [Res] one extrusion. *)

Lemma red_comb_cw n (e : pch n) (Q Q' : procP n) :
  ltswP e Q Q' -> forall K, red (((pflip e) !․ K) ∥ Q) (K ∥ Q').
Proof.
  move=> H. elim: H => {n e Q Q'}.
  - move=> n c K K0. have HR := R_CW (pflip c) K0 K.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c P P' Q Hw IH K.
    apply: (red_congL (cong_pullL (pflip c !․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ P') ∥ Q)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullL K P' Q).
  - move=> n c P Q Q' Hw IH K.
    apply: (red_congL (cong_pullR (pflip c !․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ Q') ∥ P)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullR K P Q').
  - move=> n c P P' Hw IH K.
    apply: (red_congL (CGC_one (CG_PComm ((pflip c) !․ K) ((ν)P)))).
    apply: (red_congL (CGC_one (CG_Extr P ((pflip c) !․ K)))).
    have HR : ((ν)((⟨shift⟩K) ∥ P')) ≡ (K ∥ ((ν)P')).
      apply: (CGC_trans (Q := (ν)(P' ∥ (⟨shift⟩K)))).
      * apply: cong_nu. apply: CGC_one. exact: CG_PComm.
      * apply: (CGC_trans (Q := ((ν)P') ∥ K)).
        -- apply: CGC_one. exact: (CG_Extr' P' K).
        -- apply: CGC_one. exact: CG_PComm.
    apply: (red_congR (Q2 := (ν)((⟨shift⟩K) ∥ P'))); last exact: HR.
    apply: R_Res.
    apply: (red_congL (CGC_one (CG_PComm P (⟨shift⟩(pflip c !․ K))))).
    rewrite /= pren_flip.
    exact: (IH (⟨shift⟩K)).
Qed.

Lemma red_comb_dr n (e d : pch n) (Q Q' : procP n) :
  ltsrP e d Q Q' -> forall K, red (((pflip e) ! d ․ K) ∥ Q) (K ∥ Q').
Proof.
  move=> H. elim: H => {n e d Q Q'}.
  - move=> n c y r K K0. have HR := R_Del (pflip c) y r K0 K.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c d P P' Q Hr IH K.
    apply: (red_congL (cong_pullL (pflip c ! d ․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ P') ∥ Q)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullL K P' Q).
  - move=> n c d P Q Q' Hr IH K.
    apply: (red_congL (cong_pullR (pflip c ! d ․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ Q') ∥ P)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullR K P Q').
  - move=> n c d P P' Hr IH K.
    apply: (red_congL (CGC_one (CG_PComm ((pflip c) ! d ․ K) ((ν)P)))).
    apply: (red_congL (CGC_one (CG_Extr P ((pflip c) ! d ․ K)))).
    have HR : ((ν)((⟨shift⟩K) ∥ P')) ≡ (K ∥ ((ν)P')).
      apply: (CGC_trans (Q := (ν)(P' ∥ (⟨shift⟩K)))).
      * apply: cong_nu. apply: CGC_one. exact: CG_PComm.
      * apply: (CGC_trans (Q := ((ν)P') ∥ K)).
        -- apply: CGC_one. exact: (CG_Extr' P' K).
        -- apply: CGC_one. exact: CG_PComm.
    apply: (red_congR (Q2 := (ν)((⟨shift⟩K) ∥ P'))); last exact: HR.
    apply: R_Res.
    apply: (red_congL (CGC_one (CG_PComm P (⟨shift⟩(pflip c ! d ․ K))))).
    rewrite /= pren_flip.
    exact: (IH (⟨shift⟩K)).
Qed.

Lemma red_comb_sb n (e : pch n) (b : bool) (Q Q' : procP n) :
  ltsbrP e b Q Q' -> forall K, red (((pflip e) ◁ b ․ K) ∥ Q) (K ∥ Q').
Proof.
  move=> H. elim: H => {n e b Q Q'}.
  - move=> n c b K1 K2 K. have HR := R_SB (pflip c) b K K1 K2.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c b P P' Q Hbr IH K.
    apply: (red_congL (cong_pullL (pflip c ◁ b ․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ P') ∥ Q)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullL K P' Q).
  - move=> n c b P Q Q' Hbr IH K.
    apply: (red_congL (cong_pullR (pflip c ◁ b ․ K) P Q)).
    apply: (red_congR (Q2 := (K ∥ Q') ∥ P)).
    + apply: R_Par. exact: IH.
    + apply: cong_sym. exact: (cong_pullR K P Q').
  - move=> n c b P P' Hbr IH K.
    apply: (red_congL (CGC_one (CG_PComm ((pflip c) ◁ b ․ K) ((ν)P)))).
    apply: (red_congL (CGC_one (CG_Extr P ((pflip c) ◁ b ․ K)))).
    have HR : ((ν)((⟨shift⟩K) ∥ P')) ≡ (K ∥ ((ν)P')).
      apply: (CGC_trans (Q := (ν)(P' ∥ (⟨shift⟩K)))).
      * apply: cong_nu. apply: CGC_one. exact: CG_PComm.
      * apply: (CGC_trans (Q := ((ν)P') ∥ K)).
        -- apply: CGC_one. exact: (CG_Extr' P' K).
        -- apply: CGC_one. exact: CG_PComm.
    apply: (red_congR (Q2 := (ν)((⟨shift⟩K) ∥ P'))); last exact: HR.
    apply: R_Res.
    apply: (red_congL (CGC_one (CG_PComm P (⟨shift⟩(pflip c ◁ b ․ K))))).
    rewrite /= pren_flip.
    exact: (IH (⟨shift⟩K)).
Qed.

(** *** Step 3: main combination lemmas

    Both endpoints general.  Induction on the sender derivation (the
    receiver quantified inside the motive); the [Pfx] case delegates
    to Step 2, each frame case is an [ex] rearrangement, [Res] one
    extrusion with the receiver conjugated by [lts*P_ren]. *)

Lemma red_cw n (c : pch n) P P' :
  ltscP c P P' -> forall Q Q', ltswP (pflip c) Q Q' -> red (P ∥ Q) (P' ∥ Q').
Proof.
  move=> H. elim: H => {n c P P'}.
  - move=> n c K Q Q' Hw. have HR := red_comb_cw Hw K.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c P P' Q Hc IH Q0 Q' Hw.
    apply: (red_congL (cong_exL P Q Q0)).
    apply: (red_congR (Q2 := (P' ∥ Q') ∥ Q)).
    + apply: R_Par. exact: (IH Q0 Q' Hw).
    + exact: (cong_exL P' Q' Q).
  - move=> n c P Q Q' Hc IH Q0 Q'0 Hw.
    apply: (red_congL (cong_exR P Q Q0)).
    apply: (red_congR (Q2 := (Q' ∥ Q'0) ∥ P)).
    + apply: R_Par. exact: (IH Q0 Q'0 Hw).
    + apply: cong_sym. exact: (cong_exR P Q' Q'0).
  - move=> n c P P' Hc IH Q Q' Hw.
    apply: (red_congL (CGC_one (CG_Extr P Q))).
    apply: (red_congR (Q2 := (ν)(P' ∥ (⟨shift⟩Q')))); last exact: (CGC_one (CG_Extr' P' Q')).
    apply: R_Res.
    have HW := ltswP_ren Hw shift. rewrite pren_flip in HW.
    exact: (IH _ _ HW).
Qed.

Lemma red_dr n (c d : pch n) P P' :
  ltsfP c d P P' -> forall Q Q', ltsrP (pflip c) d Q Q' -> red (P ∥ Q) (P' ∥ Q').
Proof.
  move=> H. elim: H => {n c d P P'}.
  - move=> n c d K Q Q' Hr. have HR := red_comb_dr Hr K.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c d P P' Q Hf IH Q0 Q' Hr.
    apply: (red_congL (cong_exL P Q Q0)).
    apply: (red_congR (Q2 := (P' ∥ Q') ∥ Q)).
    + apply: R_Par. exact: (IH Q0 Q' Hr).
    + exact: (cong_exL P' Q' Q).
  - move=> n c d P Q Q' Hf IH Q0 Q'0 Hr.
    apply: (red_congL (cong_exR P Q Q0)).
    apply: (red_congR (Q2 := (Q' ∥ Q'0) ∥ P)).
    + apply: R_Par. exact: (IH Q0 Q'0 Hr).
    + apply: cong_sym. exact: (cong_exR P Q' Q'0).
  - move=> n c d P P' Hf IH Q Q' Hr.
    apply: (red_congL (CGC_one (CG_Extr P Q))).
    apply: (red_congR (Q2 := (ν)(P' ∥ (⟨shift⟩Q')))); last exact: (CGC_one (CG_Extr' P' Q')).
    apply: R_Res.
    have HR := ltsrP_ren Hr shift. rewrite pren_flip in HR.
    exact: (IH _ _ HR).
Qed.

Lemma red_sb n (c : pch n) (b : bool) P P' :
  ltsselP c b P P' -> forall Q Q', ltsbrP (pflip c) b Q Q' -> red (P ∥ Q) (P' ∥ Q').
Proof.
  move=> H. elim: H => {n c b P P'}.
  - move=> n c b K Q Q' Hbr. have HR := red_comb_sb Hbr K.
    rewrite pflip_invol in HR. exact: HR.
  - move=> n c b P P' Q Hs IH Q0 Q' Hbr.
    apply: (red_congL (cong_exL P Q Q0)).
    apply: (red_congR (Q2 := (P' ∥ Q') ∥ Q)).
    + apply: R_Par. exact: (IH Q0 Q' Hbr).
    + exact: (cong_exL P' Q' Q).
  - move=> n c b P Q Q' Hs IH Q0 Q'0 Hbr.
    apply: (red_congL (cong_exR P Q Q0)).
    apply: (red_congR (Q2 := (Q' ∥ Q'0) ∥ P)).
    + apply: R_Par. exact: (IH Q0 Q'0 Hbr).
    + apply: cong_sym. exact: (cong_exR P Q' Q'0).
  - move=> n c b P P' Hs IH Q Q' Hbr.
    apply: (red_congL (CGC_one (CG_Extr P Q))).
    apply: (red_congR (Q2 := (ν)(P' ∥ (⟨shift⟩Q')))); last exact: (CGC_one (CG_Extr' P' Q')).
    apply: R_Res.
    have HB := ltsbrP_ren Hbr shift. rewrite pren_flip in HB.
    exact: (IH _ _ HB).
Qed.

(** Bound delegation: the sender opens its own restriction, the
    receiver takes the fresh endpoint under one extra binder.  The
    [Res] case needs the double-shift commutation [psubst_shift_comm]
    and the binder exchange [CG_NuSwap]/[psubst_swap_shift]. *)

Lemma red_br n (c : pch n) (r : pol) P (P' : procP n.+1) :
  ltsbP c r P P' ->
  forall Q Q', ltsrP (pshift (pflip c)) (zero, r) (⟨shift⟩ Q) Q' ->
  red (P ∥ Q) ((ν) (P' ∥ Q')).
Proof.
  move=> H. elim: H => {n c r P P'}.
  - move=> n c r P1 P' Hf Q Q' Hr.
    apply: (red_congL (CGC_one (CG_Extr P1 Q))).
    apply: R_Res.
    apply: (red_dr Hf).
    rewrite -pshift_pflip. exact: Hr.
  - move=> n c r P1 P1' F Hb IH Q Q' Hr.
    apply: (red_congL (cong_exL P1 F Q)).
    apply: (red_congR (Q2 := ((ν)(P1' ∥ Q')) ∥ F)).
    + apply: R_Par. exact: (IH Q Q' Hr).
    + apply: (CGC_trans (Q := (ν)((P1' ∥ Q') ∥ ⟨shift⟩F))).
      * apply: CGC_one. exact: (CG_Extr (P1' ∥ Q') F).
      * apply: cong_nu. exact: (cong_exL P1' Q' (⟨shift⟩F)).
  - move=> n c r P S S' Hb IH Q Q' Hr.
    apply: (red_congL (cong_exR P S Q)).
    apply: (red_congR (Q2 := ((ν)(S' ∥ Q')) ∥ P)).
    + apply: R_Par. exact: (IH Q Q' Hr).
    + apply: (CGC_trans (Q := (ν)((S' ∥ Q') ∥ ⟨shift⟩P))).
      * apply: CGC_one. exact: (CG_Extr (S' ∥ Q') P).
      * apply: cong_nu. apply: cong_sym. exact: (cong_exR (⟨shift⟩P) S' Q').
  - move=> n c r P1 P1'' Hb IH Q Q' Hr.
    apply: (red_congL (CGC_one (CG_Extr P1 Q))).
    have HR := ltsrP_ren Hr (up_ch shift).
    rewrite pren_up_pshift pren_flip pren_up_zero psubst_shift_comm in HR.
    have Hchain : ((ν)((ν)(⟨swap_ch zero one⟩P1'') ∥ Q')) ≡ ((ν)((ν)(P1'' ∥ ⟨up_ch shift⟩Q'))).
      apply: (CGC_trans (Q := (ν)(ν)(⟨swap_ch zero one⟩P1'' ∥ ⟨shift⟩Q'))).
      - apply: cong_nu. apply: CGC_one. exact: (CG_Extr (⟨swap_ch zero one⟩P1'') Q').
      - apply: (CGC_trans (Q := (ν)(ν)(⟨swap_ch zero one⟩(⟨swap_ch zero one⟩P1'' ∥ ⟨shift⟩Q')))).
        + apply: CGC_one. exact: (CG_NuSwap (⟨swap_ch zero one⟩P1'' ∥ ⟨shift⟩Q')).
        + rewrite /= psubst_swap01_invol psubst_swap_shift. exact: cong_refl.
    apply: (red_congR (Q2 := (ν)((ν)(P1'' ∥ ⟨up_ch shift⟩Q')))); last exact: (cong_sym Hchain).
    apply: R_Res.
    exact: (IH (⟨shift⟩Q) (⟨up_ch shift⟩Q') HR).
Qed.

(** *** Step 4: the harmony lemma

    Induction on the τ-step: the three congruence rules are direct
    [red] congruences, the eight communication rules land in the
    combination lemmas, the six mirrored ones after a [CG_PComm]
    conjugation. *)

Theorem lts_impl_red n (P Q : procP n) : ltstP P Q -> red P Q.
Proof.
  move=> H. elim: H => {n P Q}.
  - move=> n P P' Q _ IH. exact: R_Par IH.
  - move=> n P Q Q' _ IH. exact: red_parR IH.
  - move=> n P P' _ IH. exact: R_Res IH.
  - move=> n c P P' Q Q' Hc Hw. exact: (red_cw Hc Hw).
  - move=> n c P P' Q Q' Hw Hc.
    apply: (red_congL (CGC_one (CG_PComm P Q))).
    apply: (red_congR (Q2 := Q' ∥ P')); last exact: (CGC_one (CG_PComm Q' P')).
    exact: (red_cw Hc Hw).
  - move=> n c d P P' Q Q' Hf Hr. exact: (red_dr Hf Hr).
  - move=> n c d P P' Q Q' Hr Hf.
    apply: (red_congL (CGC_one (CG_PComm P Q))).
    apply: (red_congR (Q2 := Q' ∥ P')); last exact: (CGC_one (CG_PComm Q' P')).
    exact: (red_dr Hf Hr).
  - move=> n c r P P' Q Q' Hb Hr. exact: (red_br Hb Hr).
  - move=> n c r P P' Q Q' Hr Hb.
    apply: (red_congL (CGC_one (CG_PComm P Q))).
    apply: (red_congR (Q2 := (ν)(Q' ∥ P'))); last exact: (cong_nu (CGC_one (CG_PComm Q' P'))).
    exact: (red_br Hb Hr).
  - move=> n c b P P' Q Q' Hs Hbr. exact: (red_sb Hs Hbr).
  - move=> n c b P P' Q Q' Hbr Hs.
    apply: (red_congL (CGC_one (CG_PComm P Q))).
    apply: (red_congR (Q2 := Q' ∥ P')); last exact: (CGC_one (CG_PComm Q' P')).
    exact: (red_sb Hs Hbr).
Qed.

(** Sanity: a free-channel close/wait synchronization reduces.  This
    documents the operational reading the LTS gives [red]. *)
Example harmony_smoke n (x : ch n) :
  red (((x, pos) !․ ∅) ∥ ((x, neg) ?․ ∅)) (∅ ∥ ∅).
Proof. apply: lts_impl_red. exact: PT_CW (PC_Pfx (x, pos) ∅) (PW_Pfx (x, neg) ∅). Qed.

(** ** Structural congruence respects the visible LTS

    The [red] ⇒ τ direction rests on [cong] being a strong bisimulation
    for every transition family.  Here are the FIVE mechanical
    visible-action families -- close [ltscP], wait [ltswP], free send
    [ltsfP], select [ltsselP], branch [ltsbrP].  Each [cong1_resp_lts*]
    is an induction on the one-layer congruence (19 cases): the
    parallel-monoid axioms and scope-extrusion axioms rearrange the
    transition through the residual-carrying inversions
    [lts*P_parE]/[lts*P_resE] below, the [pinv_*] grid of [PolLTS.v]
    refutes the mismatched prefixes and fires the matching one, and the
    forward/backward renamings ([lts*P_ren], [lts*P_ren_inv]) close the
    [CG_Extr]/[CG_Extr']/[CG_NuSwap] frames -- the [CG_Extr'] frame
    needs the injectivity of [pren shift] on subjects (and objects, for
    free send), the [CG_NuSwap] frame the fixity of a doubly-shifted
    subject under the top swap.  The [cong_resp_lts*] lifts chain the
    existential witnesses along the transitive closure. *)
Lemma pren_shift_inj n (c0 c : pch n) : pren shift c0 = pren shift c -> c0 = c.
Proof.
  case: c0 => x0 r0; case: c => x r H.
  by injection H => -> ->.
Qed.

Lemma pren_swap01_pshift2 n (c : pch n) :
  pren (swap_ch zero one) (pshift (pshift c)) = pshift (pshift c).
Proof. by case: c => x r. Qed.

(** *** Residual-carrying inversions: close *)

Lemma ltscP_parE n (c : pch n) A B R :
  ltscP c (A ∥ B) R ->
  (exists A', R = A' ∥ B /\ ltscP c A A') \/
  (exists B', R = A ∥ B' /\ ltscP c B B').
Proof. move=> H. pinv H; [left|right]; eexists; (split; [reflexivity|eassumption]). Qed.

Lemma ltscP_resE n (c : pch n) (P : procP n.+1) R :
  ltscP c ((ν) P) R -> exists P', R = (ν) P' /\ ltscP (pshift c) P P'.
Proof. move=> H. pinv H. eexists; (split; [reflexivity|eassumption]). Qed.

(** *** Close *)

Lemma cong1_resp_ltsc n (P Q : procP n) :
  cong1 P Q -> forall (c : pch n) P', ltscP c P P' ->
  exists Q', ltscP c Q Q' /\ P' ≡ Q'.
Proof.
  move=> H. elim: H => {n P Q}.
  - (* CG_PComm *)
    move=> n P Q c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists (Q ∥ A'); split; [exact: PC_ParR HA | apply: CGC_one; exact: CG_PComm].
    + exists (B' ∥ P); split; [exact: PC_ParL HB | apply: CGC_one; exact: CG_PComm].
  - (* CG_PAssoc *)
    move=> n P Q R c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltscP_parE HA) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists (A2 ∥ (Q ∥ R)); split; [exact: PC_ParL HA2 | apply: CGC_one; exact: CG_PAssoc].
      * exists (P ∥ (B2 ∥ R)); split;
          [apply: PC_ParR; exact: PC_ParL HB2 | apply: CGC_one; exact: CG_PAssoc].
    + exists (P ∥ (Q ∥ B')); split;
        [apply: PC_ParR; exact: PC_ParR HB | apply: CGC_one; exact: CG_PAssoc].
  - (* CG_PAssoc' *)
    move=> n P Q R c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists ((A' ∥ Q) ∥ R); split;
        [apply: PC_ParL; exact: PC_ParL HA | apply: CGC_one; exact: CG_PAssoc'].
    + case: (ltscP_parE HB) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists ((P ∥ A2) ∥ R); split;
          [apply: PC_ParL; exact: PC_ParR HA2 | apply: CGC_one; exact: CG_PAssoc'].
      * exists ((P ∥ Q) ∥ B2); split;
          [exact: PC_ParR HB2 | apply: CGC_one; exact: CG_PAssoc'].
  - (* CG_PZero *)
    move=> n P c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists A'; split; [exact: HA | apply: CGC_one; exact: CG_PZero].
    + by case: (pinv_c_end HB).
  - (* CG_PZero' *)
    move=> n P c P' HT.
    exists (P' ∥ ∅); split; [apply: PC_ParL; exact: HT | apply: CGC_one; exact: CG_PZero'].
  - (* CG_NuZero *)
    move=> n c P' HT.
    case: (ltscP_resE HT) => R1 [_ H0]. by case: (pinv_c_end H0).
  - (* CG_NuZero' *)
    move=> n c P' HT. by case: (pinv_c_end HT).
  - (* CG_Extr *)
    move=> n P Q c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltscP_resE HA) => P1 [-> HP1].
      exists ((ν) (P1 ∥ ⟨shift⟩ Q)); split.
      * apply: PC_Res. apply: PC_ParL. exact: HP1.
      * apply: CGC_one. exact: CG_Extr.
    + exists ((ν) (P ∥ ⟨shift⟩ B')); split.
      * apply: PC_Res. apply: PC_ParR. exact: (ltscP_ren HB shift).
      * apply: CGC_one. exact: CG_Extr.
  - (* CG_Extr' *)
    move=> n P Q c P' HT.
    case: (ltscP_resE HT) => R1 [-> H1].
    case: (ltscP_parE H1) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists (((ν) A') ∥ Q); split.
      * apply: PC_ParL. exact: PC_Res HA.
      * apply: CGC_one. exact: CG_Extr'.
    + case: (ltscP_ren_inv HB erefl) => c0 [Q0 [Ec -> HQ0]].
      have Ecc := pren_shift_inj (esym Ec). subst c0.
      exists (((ν) P) ∥ Q0); split.
      * apply: PC_ParR. exact: HQ0.
      * apply: CGC_one. exact: CG_Extr'.
  - (* CG_NuSwap *)
    move=> n P c P' HT.
    case: (ltscP_resE HT) => R1 [-> H1].
    case: (ltscP_resE H1) => R2 [-> H2].
    exists ((ν) (ν) (⟨swap_ch zero one⟩ R2)); split.
    + apply: PC_Res. apply: PC_Res.
      have := ltscP_ren H2 (swap_ch zero one).
      by rewrite pren_swap01_pshift2.
    + apply: CGC_one. exact: CG_NuSwap.
  - (* CG_Zero *)
    move=> n c P' HT. by case: (pinv_c_end HT).
  - (* CG_ParL *)
    move=> n P0 P0' Q Hc IH c P' HT.
    case: (ltscP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (IH c A' HA) => A2 [HA2 HcA].
      exists (A2 ∥ Q); split; [exact: PC_ParL HA2 | exact: cong_parL Q HcA].
    + exists (P0' ∥ B'); split; [exact: PC_ParR HB | exact: cong_parL B' (CGC_one Hc)].
  - (* CG_Res *)
    move=> n P0 P0' Hc IH c P' HT.
    case: (ltscP_resE HT) => R1 [-> HR1].
    case: (IH (pshift c) R1 HR1) => R2 [HR2 HcR].
    exists ((ν) R2); split; [exact: PC_Res HR2 | exact: cong_nu HcR].
  - (* CG_Wait *)
    move=> n c0 K K' Hc IH c P' HT. by case: (pinv_c_wait HT).
  - (* CG_Close *)
    move=> n c0 K K' Hc IH c P' HT.
    case: (pinv_c_closeF HT) => Ec ->.
    exists K'; split; [rewrite Ec; exact: PC_Pfx | apply: CGC_one; exact: Hc].
  - (* CG_Ins *)
    move=> n c0 r K K' Hc IH c P' HT. by case: (pinv_c_ins HT).
  - (* CG_Del *)
    move=> n c0 d K K' Hc IH c P' HT. by case: (pinv_c_del HT).
  - (* CG_Sel *)
    move=> n c0 b K K' Hc IH c P' HT. by case: (pinv_c_sel HT).
  - (* CG_Bra *)
    move=> n c0 K1 K1' K2 K2' Hc1 IH1 Hc2 IH2 c P' HT. by case: (pinv_c_bra HT).
Qed.

