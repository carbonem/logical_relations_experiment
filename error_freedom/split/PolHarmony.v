(** * Reduction semantics, structural congruence, and the harmony lemma

    A standard reduction semantics for the polarized calculus --
    structural congruence [cong] generated by the usual axioms
    (commutative monoid laws for ∥, inert (ν)∅, scope extrusion,
    binder exchange), and reduction [red] with one axiom per
    communication kind, closed under ∥, ν and [cong].

    The harmony lemma relates [red] to the LTS of [PolLTS.v]:

      lts_impl_red : ltstP P Q  ->  red P Q
      red_impl_lts : red P Q    ->  exists Q', ltstP P Q' /\ cong Q Q'

    The asymmetry is inherent: [red] absorbs [cong] by construction,
    while the LTS neither rearranges parallel components nor
    garbage-collects, so its τ-successor is only [cong]-equal to the
    reduction's.

    Design notes, following Kan & Lenglet's Scorpion development
    (FORTE 2026), adapted to well-scoped syntax:

    - [cong1] is a ONE-LAYER congruence: the structural axioms in
      symmetric pairs (binder exchange is self-symmetric via the
      involution [swap_ch zero one]), plus one congruence rule per
      constructor, with only the LEFT parallel component congruent.
      [cong] is the transitive closure alone; reflexivity, symmetry,
      and right-parallel congruence are lemmas.  Inductions over
      [cong] therefore have exactly two cases.

    - No normal forms.  The τ ⇒ red direction goes by nested
      induction on the two synchronizing derivations ("combination
      lemmas"), peeling one binder at a time: each [Res] case is one
      scope extrusion and one [shift], each frame case one
      rearrangement.  Iterated-ν decompositions, and their [shiftn]
      arithmetic -- the bulk of Scorpion's algebra, and worse here
      because well-scoped ν changes the index -- never appear.

    - The red ⇒ τ direction rests on [cong] being a strong
      bisimulation for all seven transition families
      ([cong_resp_lts*]).  Renamings enter through the forward
      preservation lemmas [lts*P_ren] (restored from the attic) and
      the backward inversions of [PolEquiv.v]. *)

