            | [ [c [d [A' [B' [-> HA HB]]]]]
              | [ [c [d [A' [B' [-> HA HB]]]]]
                | [ [c [r [A' [B' [-> HA HB]]]]]
                  | [ [c [r [A' [B' [-> HA HB]]]]]
                    | [ [c [b [A' [B' [-> HA HB]]]]]
                      | [c [b [A' [B' [-> HA HB]]]] ]]]]]]]]]].
    + (* inner ParL: τ of P *)
      exists (((ν) A') ∥ Q); split.
      * apply: PT_ParL. apply: PT_Res. exact: HA.
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner ParR: τ of ⟨shift⟩Q *)
      case: (ltstP_ren_inv HB (s := @shift n) (P := Q) (@shift_name_inj n) erefl) => Q0 [-> HQ0].
      exists (((ν) P) ∥ Q0); split.
      * apply: PT_ParR. exact: HQ0.
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner CW: ⟨shift⟩Q waits (flipped side) *)
      case: (ltswP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec -> HW0]].
      rewrite (extr'_flip Ec) in HA.
      exists (((ν) A') ∥ P0); split.
      * apply: (PT_CW (c := pflip c0)); [exact: (PC_Res HA) | rewrite pflip_invol; exact: HW0].
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner WC: ⟨shift⟩Q closes (non-flipped side) *)
      case: (ltscP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec -> HC0]].
      rewrite Ec -pren_flip in HA.
      exists (((ν) A') ∥ P0); split.
      * apply: (PT_WC (c := c0)); [exact: (PW_Res HA) | exact: HC0].
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner DR: P free-sends d, ⟨shift⟩Q receives d *)
      case: (ltsrP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec ErB Hu]].
      rewrite (extr'_flip Ec) in HA.
      case: d HA HB ErB Hu => [[d1'|] dr] HA HB ErB Hu.
      * (* object shift d1' : free delegation, PT_DR, residual CG_Extr' *)
        have EY : B' = ⟨shift⟩ (psubst (scons d1' id_ren) P0).
          rewrite ErB psubst_comp. by apply: psubst_ext => -[z|].
        exists (((ν) A') ∥ psubst (scons d1' id_ren) P0); split.
        -- apply: (PT_DR (c := pflip c0) (d := (d1', dr))).
           ++ apply: PF_Res. exact: HA.
           ++ rewrite pflip_invol. exact: (Hu d1').
        -- rewrite EY. apply: CGC_one. exact: CG_Extr'.
      * (* object zero : bound delegation, PT_BR, residual cong_refl *)
        exists ((ν) (A' ∥ B')); split; last exact: cong_refl.
        apply: (PT_BR (c := pflip c0)).
        -- apply: PB_Open. exact: HA.
        -- rewrite pflip_invol. rewrite Ec in HB. exact: HB.
    + (* inner RD: P receives d, ⟨shift⟩Q free-sends d (object always a shift image) *)
      case: (ltsfP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [d0 [P0 [Ec Ed -> Hf0]]].
      rewrite Ec Ed -pren_flip in HA.
      exists (((ν) A') ∥ P0); split.
      * apply: (PT_RD (c := c0)); first (apply: PR_Res; exact: HA).
        exact: Hf0.
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner BR: P bound-sends, ⟨shift⟩⟨shift⟩Q receives — outer (ν)P bound-sends via PB_Res *)
      have HBeq : psubst (up_ch shift) (⟨shift⟩ Q) = ⟨shift⟩ (⟨shift⟩ Q) by rewrite psubst_shift_comm.
      case: (ltsrP_ren_inv HB (s := up_ch shift) (P := ⟨shift⟩ Q) HBeq) => c1 [P0 [Ec1 ErB Hu]].
      case: (pren_up_image_pshift (esym Ec1)) => c3 [Ec1' Hc3].
      rewrite (extr'_flip (esym Hc3)) in HA.
      have HQ := Hu zero. rewrite Ec1' in HQ.
      have EB : B' = ⟨up_ch shift⟩ (psubst (scons zero id_ren) P0).
        rewrite ErB psubst_comp. by apply: psubst_ext => -[w|].
      have Hchain : ((ν) ((ν) (⟨swap_ch zero one⟩ A') ∥ psubst (scons zero id_ren) P0))
                  ≡ ((ν) (ν) (A' ∥ ⟨up_ch shift⟩ (psubst (scons zero id_ren) P0))).
        apply: (CGC_trans (Q := (ν) (ν) (⟨swap_ch zero one⟩ A' ∥ ⟨shift⟩ (psubst (scons zero id_ren) P0)))).
        -- apply: cong_nu. apply: CGC_one.
           exact: (CG_Extr (⟨swap_ch zero one⟩ A') (psubst (scons zero id_ren) P0)).
        -- apply: (CGC_trans (Q := (ν) (ν) (⟨swap_ch zero one⟩
                    (⟨swap_ch zero one⟩ A' ∥ ⟨shift⟩ (psubst (scons zero id_ren) P0))))).
           ++ apply: CGC_one. exact: (CG_NuSwap _).
           ++ rewrite /= psubst_swap01_invol psubst_swap_shift. exact: cong_refl.
      exists ((ν) ((ν) (⟨swap_ch zero one⟩ A') ∥ psubst (scons zero id_ren) P0)); split.
      * apply: (PT_BR (c := pflip c3)).
        -- apply: PB_Res. exact: HA.
        -- rewrite pflip_invol. exact: HQ.
      * rewrite EB. apply: cong_sym. exact: Hchain.
    + (* inner RB: P receives ⟨shift⟩P, ⟨shift⟩Q bound-sends — outer P receives, Q bound-sends *)
      case: (ltsbP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec -> Hb0]].
      rewrite Ec -pren_flip pren_shift_pshift in HA.
      have HAR := ltsrP_ren HA (swap_ch zero one).
      rewrite pren_swap01_pshift2 pren_swap_zero psubst_swap_shift in HAR.
      have Hchain : ((ν) ((ν) (⟨swap_ch zero one⟩ A') ∥ P0))
                  ≡ ((ν) (ν) (A' ∥ ⟨up_ch shift⟩ P0)).
        apply: (CGC_trans (Q := (ν) (ν) (⟨swap_ch zero one⟩ A' ∥ ⟨shift⟩ P0))).
        -- apply: cong_nu. apply: CGC_one. exact: (CG_Extr (⟨swap_ch zero one⟩ A') P0).
        -- apply: (CGC_trans (Q := (ν) (ν) (⟨swap_ch zero one⟩ (⟨swap_ch zero one⟩ A' ∥ ⟨shift⟩ P0)))).
           ++ apply: CGC_one. exact: (CG_NuSwap _).
           ++ rewrite /= psubst_swap01_invol psubst_swap_shift. exact: cong_refl.
      exists ((ν) ((ν) (⟨swap_ch zero one⟩ A') ∥ P0)); split.
      * apply: (PT_RB (c := c0)); last exact: Hb0.
        rewrite psubst_shift_res. apply: PR_Res. exact: HAR.
      * apply: cong_sym. exact: Hchain.
    + (* inner SB: ⟨shift⟩Q branches (flipped side) *)
      case: (ltsbrP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec -> Hbr0]].
      rewrite (extr'_flip Ec) in HA.
      exists (((ν) A') ∥ P0); split.
      * apply: (PT_SB (c := pflip c0)); [exact: (PS_Res HA) | rewrite pflip_invol; exact: Hbr0].
      * apply: CGC_one. exact: CG_Extr'.
    + (* inner BS: ⟨shift⟩Q selects (non-flipped side) *)
      case: (ltsselP_ren_inv HB (s := @shift n) (P := Q) erefl) => c0 [P0 [Ec -> Hs0]].
      rewrite Ec -pren_flip in HA.
      exists (((ν) A') ∥ P0); split.
      * apply: (PT_BS (c := c0)); [exact: (PBR_Res HA) | exact: Hs0].
      * apply: CGC_one. exact: CG_Extr'.
  - (* CG_NuSwap *)
    move=> n P T HT.
    case: (ltstP_resE HT) => T1 [-> H1].
    case: (ltstP_resE H1) => T2 [-> H2].
    exists ((ν) (ν) (⟨swap_ch zero one⟩ T2)); split.
    + apply: PT_Res. apply: PT_Res. exact: (ltstP_ren H2 (swap_ch zero one)).
    + apply: CGC_one. exact: (CG_NuSwap T2).
  - (* CG_Zero *) move=> n T HT. by case: (pinv_t_end HT).
  - (* CG_ParL *)
    move=> n P P' Q Hc IH T HT.
    case: (ltstP_parE HT) =>
      [ [A' [-> HA]]
      | [ [B' [-> HB]]
        | [ [c [A' [B' [-> HA HB]]]]
          | [ [c [A' [B' [-> HA HB]]]]
            | [ [c [d [A' [B' [-> HA HB]]]]]
              | [ [c [d [A' [B' [-> HA HB]]]]]
                | [ [c [r [A' [B' [-> HA HB]]]]]
                  | [ [c [r [A' [B' [-> HA HB]]]]]
                    | [ [c [b [A' [B' [-> HA HB]]]]]
                      | [c [b [A' [B' [-> HA HB]]]] ]]]]]]]]]].
    + case: (IH A' HA) => A2 [HA2 HcA].
      exists (A2 ∥ Q); split; [exact: PT_ParL HA2 | exact: cong_parL Q HcA].
    + exists (P' ∥ B'); split; [exact: PT_ParR HB | exact: cong_parL B' (CGC_one Hc)].
    + case: (cong1_resp_ltsc Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_CW HA2 HB | exact: cong_parL B' HcA].
    + case: (cong1_resp_ltsw Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_WC HA2 HB | exact: cong_parL B' HcA].
    + case: (cong1_resp_ltsf Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_DR HA2 HB | exact: cong_parL B' HcA].
    + case: (cong1_resp_ltsr Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_RD HA2 HB | exact: cong_parL B' HcA].
    + case: (cong1_resp_ltsb Hc HA) => A2 [HA2 HcA].
      exists ((ν) (A2 ∥ B')); split;
        [exact: PT_BR HA2 HB | exact: cong_nu (cong_parL B' HcA)].
    + case: (cong1_resp_ltsr (cong1_ren Hc shift) HA) => A2 [HA2 HcA].
      exists ((ν) (A2 ∥ B')); split;
        [exact: PT_RB HA2 HB | exact: cong_nu (cong_parL B' HcA)].
    + case: (cong1_resp_ltssel Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_SB HA2 HB | exact: cong_parL B' HcA].
    + case: (cong1_resp_ltsbr Hc HA) => A2 [HA2 HcA].
      exists (A2 ∥ B'); split; [exact: PT_BS HA2 HB | exact: cong_parL B' HcA].
  - (* CG_Res *)
    move=> n P P' Hc IH T HT.
    case: (ltstP_resE HT) => T0 [-> HP].
    case: (IH T0 HP) => Q' [HQ' HcQ'].
    exists ((ν) Q'); split; [exact: PT_Res HQ' | exact: cong_nu HcQ'].
  - (* CG_Wait *) move=> n c K K' Hc IH T HT. by case: (pinv_t_wait HT).
  - (* CG_Close *) move=> n c K K' Hc IH T HT. by case: (pinv_t_close HT).
  - (* CG_Ins *) move=> n c r K K' Hc IH T HT. by case: (pinv_t_ins HT).
  - (* CG_Del *) move=> n c d K K' Hc IH T HT. by case: (pinv_t_del HT).
  - (* CG_Sel *) move=> n c b K K' Hc IH T HT. by case: (pinv_t_sel HT).
  - (* CG_Bra *) move=> n c K1 K1' K2 K2' Hc1 IH1 Hc2 IH2 T HT. by case: (pinv_t_bra HT).
Qed.

Lemma cong_resp_ltst n (P Q : procP n) :
  P ≡ Q -> forall P', ltstP P P' -> exists Q', ltstP Q Q' /\ P' ≡ Q'.
Proof.
  move=> H. elim: H => {n P Q}.
  - move=> n P Q Hc P' HT. exact: (cong1_resp_ltst Hc HT).
  - move=> n P Q R _ IH1 _ IH2 P' HT.
    case: (IH1 P' HT) => Q1 [HQ1 Hc1].
    case: (IH2 Q1 HQ1) => R1 [HR1 Hc2].
    exists R1; split; [exact: HR1 | exact: CGC_trans Hc1 Hc2].
Qed.

(** ** red ⇒ τ

    Each reduction axiom fires the matching [PT_*] rule directly (its
    residual is literally the reduct); [R_Par]/[R_Res] lift the IH
    through [PT_ParL]/[PT_Res]; [R_Struc] transports the τ across the
    left congruence with [cong_resp_ltst] and threads the residual
    through the congruence chain. *)
Theorem red_impl_lts n (P Q : procP n) :
  red P Q -> exists Q', ltstP P Q' /\ Q ≡ Q'.
Proof.
  move=> H. elim: H => {n P Q}.
  - move=> n c K1 K2.
    exists (K1 ∥ K2); split;
      [exact: PT_CW (PC_Pfx c K1) (PW_Pfx (pflip c) K2) | exact: cong_refl].
  - move=> n c y r K1 K2.
    exists (K1 ∥ psubst (scons y id_ren) K2); split;
      [exact: PT_DR (PF_Pfx c (y, r) K1) (PR_Pfx (pflip c) y r K2) | exact: cong_refl].
  - move=> n c b K1 K2 K3.
    exists (K1 ∥ (if b then K2 else K3)); split;
      [exact: PT_SB (PS_Pfx c b K1) (PBR_Pfx (pflip c) b K2 K3) | exact: cong_refl].
  - move=> n P P' Q Hr IH. case: IH => X [HX HcX].
    exists (X ∥ Q); split; [exact: PT_ParL HX | exact: cong_parL Q HcX].
  - move=> n P P' Hr IH. case: IH => X [HX HcX].
    exists ((ν) X); split; [exact: PT_Res HX | exact: cong_nu HcX].
  - move=> n P P2 Q2 Q H1 Hr IH H2. case: IH => X [HX HcX].
    case: (cong_resp_ltst (cong_sym H1) HX) => X' [HX' HcX'].
    exists X'; split;
      [exact: HX' | exact: CGC_trans (cong_sym H2) (CGC_trans HcX HcX')].
Qed.

(** ** Harmony *)
Theorem harmony n (P Q : procP n) :
  red P Q <-> exists Q', ltstP P Q' /\ Q ≡ Q'.
Proof.
  split.
  - exact: red_impl_lts.
  - move=> [Q' [HQ' HcQ']].
    exact: R_Struc (cong_refl P) (lts_impl_red HQ') (cong_sym HcQ').
Qed.
