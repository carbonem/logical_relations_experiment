              | [ [c [r [P2 [Q2 [-> HP HQ]]]]]
                | [ [c [r [P2 [Q2 [-> HP HQ]]]]]
                  | [ [c [b [P2 [Q2 [-> HP HQ]]]]]
                    | [c [b [P2 [Q2 [-> HP HQ]]]] ]]]]]]]]]].
  - exists (P2 ∥ (Q ∥ R)); split; [by eauto with htr | apply: CGC_one; exact: CG_PAssoc].
  - exists (P ∥ (Q2 ∥ R)); split; [by eauto with htr | apply: CGC_one; exact: CG_PAssoc].
  - exists (P2 ∥ (Q2 ∥ R)); split; [by eauto with htr | apply: CGC_one; exact: CG_PAssoc].
