Lemma cong1_resp_ltsr n (P Q : procP n) :
  cong1 P Q -> forall (c d : pch n) P', ltsrP c d P P' ->
  exists Q', ltsrP c d Q Q' /\ P' ≡ Q'.
Proof.
  move=> H. elim: H => {n P Q}.
  - (* CG_PComm *)
    move=> n P Q c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists (Q ∥ A'); split; [exact: PR_ParR HA | apply: CGC_one; exact: CG_PComm].
    + exists (B' ∥ P); split; [exact: PR_ParL HB | apply: CGC_one; exact: CG_PComm].
  - (* CG_PAssoc *)
    move=> n P Q R c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltsrP_parE HA) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists (A2 ∥ (Q ∥ R)); split; [exact: PR_ParL HA2 | apply: CGC_one; exact: CG_PAssoc].
      * exists (P ∥ (B2 ∥ R)); split;
          [apply: PR_ParR; exact: PR_ParL HB2 | apply: CGC_one; exact: CG_PAssoc].
    + exists (P ∥ (Q ∥ B')); split;
        [apply: PR_ParR; exact: PR_ParR HB | apply: CGC_one; exact: CG_PAssoc].
  - (* CG_PAssoc' *)
    move=> n P Q R c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists ((A' ∥ Q) ∥ R); split;
        [apply: PR_ParL; exact: PR_ParL HA | apply: CGC_one; exact: CG_PAssoc'].
    + case: (ltsrP_parE HB) => [[A2 [-> HA2]]|[B2 [-> HB2]]].
      * exists ((P ∥ A2) ∥ R); split;
          [apply: PR_ParL; exact: PR_ParR HA2 | apply: CGC_one; exact: CG_PAssoc'].
      * exists ((P ∥ Q) ∥ B2); split;
          [exact: PR_ParR HB2 | apply: CGC_one; exact: CG_PAssoc'].
  - (* CG_PZero *)
    move=> n P c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists A'; split; [exact: HA | apply: CGC_one; exact: CG_PZero].
    + by case: (pinv_r_end HB).
  - (* CG_PZero' *)
    move=> n P c d P' HT.
    exists (P' ∥ ∅); split; [apply: PR_ParL; exact: HT | apply: CGC_one; exact: CG_PZero'].
  - (* CG_NuZero *)
    move=> n c d P' HT.
    case: (ltsrP_resE HT) => R1 [_ H0]. by case: (pinv_r_end H0).
  - (* CG_NuZero' *)
    move=> n c d P' HT. by case: (pinv_r_end HT).
  - (* CG_Extr *)
    move=> n P Q c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (ltsrP_resE HA) => P1 [-> HP1].
      exists ((ν) (P1 ∥ ⟨shift⟩ Q)); split.
      * apply: PR_Res. apply: PR_ParL. exact: HP1.
      * apply: CGC_one. exact: CG_Extr.
    + exists ((ν) (P ∥ ⟨shift⟩ B')); split.
      * apply: PR_Res. apply: PR_ParR. exact: (ltsrP_ren HB shift).
      * apply: CGC_one. exact: CG_Extr.
  - (* CG_Extr' *)
    move=> n P Q c d P' HT.
    case: d HT => dn dr HT.
    case: (ltsrP_resE HT) => R1 [-> H1].
    case: (ltsrP_parE H1) => [[A' [-> HA]]|[B' [-> HB]]].
    + exists (((ν) A') ∥ Q); split.
      * apply: PR_ParL. exact: PR_Res HA.
      * apply: CGC_one. exact: CG_Extr'.
    + case: (ltsrP_ren_inv HB erefl) => c0 [P0 [Ec ER Hu]].
      have Ecc := pren_shift_inj (esym Ec). subst c0.
      exists (((ν) P) ∥ psubst (scons dn id_ren) P0); split.
      * apply: PR_ParR. exact: (Hu dn).
      * have EB' : B' = ⟨shift⟩ (psubst (scons dn id_ren) P0).
          rewrite ER psubst_comp. by apply: psubst_ext => -[z|].
        rewrite EB'. apply: CGC_one. exact: (CG_Extr' P (psubst (scons dn id_ren) P0)).
  - (* CG_NuSwap *)
    move=> n P c d P' HT.
    case: (ltsrP_resE HT) => R1 [-> H1].
    case: (ltsrP_resE H1) => R2 [-> H2].
    exists ((ν) (ν) (⟨swap_ch zero one⟩ R2)); split.
    + apply: PR_Res. apply: PR_Res.
      have := ltsrP_ren H2 (swap_ch zero one).
      by rewrite !pren_swap01_pshift2.
    + apply: CGC_one. exact: CG_NuSwap.
  - (* CG_Zero *)
    move=> n c d P' HT. by case: (pinv_r_end HT).
  - (* CG_ParL *)
    move=> n P0 P0' Q Hc IH c d P' HT.
    case: (ltsrP_parE HT) => [[A' [-> HA]]|[B' [-> HB]]].
    + case: (IH c d A' HA) => A2 [HA2 HcA].
      exists (A2 ∥ Q); split; [exact: PR_ParL HA2 | exact: cong_parL Q HcA].
    + exists (P0' ∥ B'); split; [exact: PR_ParR HB | exact: cong_parL B' (CGC_one Hc)].
  - (* CG_Res *)
    move=> n P0 P0' Hc IH c d P' HT.
    case: (ltsrP_resE HT) => R1 [-> HR1].
    case: (IH (pshift c) (pshift d) R1 HR1) => R2 [HR2 HcR].
    exists ((ν) R2); split; [exact: PR_Res HR2 | exact: cong_nu HcR].
  - (* CG_Wait *)
    move=> n c0 K K' Hc IH c d P' HT. by case: (pinv_r_wait HT).
  - (* CG_Close *)
    move=> n c0 K K' Hc IH c d P' HT. by case: (pinv_r_close HT).
  - (* CG_Ins FIRES *)
    move=> n c0 r K K' Hc IH c d P' HT.
    case: d HT => dn dr HT.
    case: (pinv_r_insF HT) => [-> Er ->].
    have {}Er : dr = r by exact: Er.
    subst dr.
    exists (psubst (scons dn id_ren) K'); split.
    + exact: (PR_Pfx c0 dn r K').
    + apply: CGC_one. exact: (cong1_ren Hc (scons dn id_ren)).
  - (* CG_Del *)
    move=> n c0 d0 K K' Hc IH c d P' HT. by case: (pinv_r_del HT).
  - (* CG_Sel *)
    move=> n c0 b K K' Hc IH c d P' HT. by case: (pinv_r_sel HT).
  - (* CG_Bra *)
    move=> n c0 K1 K1' K2 K2' Hc1 IH1 Hc2 IH2 c d P' HT. by case: (pinv_r_bra HT).
Qed.

