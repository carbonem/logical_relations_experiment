(** ** Structural congruence *)

(** One layer: axioms in symmetric pairs + congruence rules. *)
Inductive cong1 : forall n, procP n -> procP n -> Prop :=
(* ∥ is a commutative monoid with unit ∅ *)
| CG_PComm : forall n (P Q : procP n),
    cong1 (P ∥ Q) (Q ∥ P)
| CG_PAssoc : forall n (P Q R : procP n),
    cong1 ((P ∥ Q) ∥ R) (P ∥ (Q ∥ R))
| CG_PAssoc' : forall n (P Q R : procP n),
    cong1 (P ∥ (Q ∥ R)) ((P ∥ Q) ∥ R)
| CG_PZero : forall n (P : procP n),
    cong1 (P ∥ ∅) P
| CG_PZero' : forall n (P : procP n),
    cong1 P (P ∥ ∅)
(* inert restriction *)
| CG_NuZero : forall n,
    cong1 ((ν) ∅) (∅ : procP n)
| CG_NuZero' : forall n,
    cong1 (∅ : procP n) ((ν) ∅)
(* scope extrusion *)
| CG_Extr : forall n (P : procP n.+1) (Q : procP n),
    cong1 (((ν) P) ∥ Q) ((ν) (P ∥ ⟨shift⟩ Q))
| CG_Extr' : forall n (P : procP n.+1) (Q : procP n),
    cong1 ((ν) (P ∥ ⟨shift⟩ Q)) (((ν) P) ∥ Q)
(* binder exchange: self-symmetric, [swap_ch zero one] is an involution *)
| CG_NuSwap : forall n (P : procP n.+2),
    cong1 ((ν) (ν) P) ((ν) (ν) (⟨swap_ch zero one⟩ P))
(* congruence rules: one per constructor, left component only at ∥ *)
| CG_Zero : forall n,
    cong1 (∅ : procP n) ∅
| CG_ParL : forall n (P P' Q : procP n),
    cong1 P P' -> cong1 (P ∥ Q) (P' ∥ Q)
| CG_Res : forall n (P P' : procP n.+1),
    cong1 P P' -> cong1 ((ν) P) ((ν) P')
| CG_Wait : forall n (c : pch n) (K K' : procP n),
    cong1 K K' -> cong1 (c ?․ K) (c ?․ K')
| CG_Close : forall n (c : pch n) (K K' : procP n),
    cong1 K K' -> cong1 (c !․ K) (c !․ K')
| CG_Ins : forall n (c : pch n) (r : pol) (K K' : procP n.+1),
    cong1 K K' -> cong1 (c ?( r )․ K) (c ?( r )․ K')
| CG_Del : forall n (c d : pch n) (K K' : procP n),
    cong1 K K' -> cong1 (c ! d ․ K) (c ! d ․ K')
| CG_Sel : forall n (c : pch n) (b : bool) (K K' : procP n),
    cong1 K K' -> cong1 (c ◁ b ․ K) (c ◁ b ․ K')
| CG_Bra : forall n (c : pch n) (K1 K1' K2 K2' : procP n),
    cong1 K1 K1' -> cong1 K2 K2' ->
    cong1 (c ▷ ( K1 | K2 )) (c ▷ ( K1' | K2' )).

(** Transitive closure -- nothing else. *)
Inductive cong : forall n, procP n -> procP n -> Prop :=
| CGC_one : forall n (P Q : procP n), cong1 P Q -> cong P Q
| CGC_trans : forall n (P Q R : procP n),
    cong P Q -> cong Q R -> cong P R.

Notation "P ≡ Q" := (cong P Q) (at level 70) : pol_scope.

(** ** Reduction

    One axiom per communication kind, with the output on the left;
    the mirrored redexes come via [CG_PComm].  Bound delegation has
    no axiom: scope extrusion surfaces the free-delegation redex. *)
Inductive red : forall n, procP n -> procP n -> Prop :=
| R_CW : forall n (c : pch n) (K1 K2 : procP n),
    red ((c !․ K1) ∥ ((pflip c) ?․ K2)) (K1 ∥ K2)
| R_Del : forall n (c : pch n) (y : ch n) (r : pol)
                 (K1 : procP n) (K2 : procP n.+1),
    red ((c ! (y, r) ․ K1) ∥ ((pflip c) ?( r )․ K2))
        (K1 ∥ psubst (scons y id_ren) K2)
| R_SB : forall n (c : pch n) (b : bool) (K1 K2 K3 : procP n),
    red ((c ◁ b ․ K1) ∥ ((pflip c) ▷ ( K2 | K3 )))
        (K1 ∥ (if b then K2 else K3))
| R_Par : forall n (P P' Q : procP n),
    red P P' -> red (P ∥ Q) (P' ∥ Q)
| R_Res : forall n (P P' : procP n.+1),
    red P P' -> red ((ν) P) ((ν) P')
| R_Struc : forall n (P P' Q' Q : procP n),
    P ≡ P' -> red P' Q' -> Q' ≡ Q -> red P Q.

(** ** Swap involution

    [swap_ch zero one] is a self-inverse renaming of the top two
    names; its process action is therefore an involution.  The
    binder-exchange axiom [CG_NuSwap] is self-symmetric through it. *)

Lemma swap01_invol n (z : ch n.+2) :
  swap_ch zero one (swap_ch zero one z) = z.
Proof. by case: z => [[z|]|]. Qed.

Lemma psubst_swap01_invol n (P : procP n.+2) :
  psubst (swap_ch zero one) (psubst (swap_ch zero one) P) = P.
Proof.
  rewrite psubst_comp -[RHS]psubst_id.
  apply: psubst_ext => z. exact: swap01_invol.
Qed.

(** ** Reflexivity *)

Lemma cong1_refl n (P : procP n) : cong1 P P.
Proof.
  elim: P.
  - move=> n'. exact: CG_Zero.
  - move=> n' c K IH. exact: CG_Wait IH.
  - move=> n' c K IH. exact: CG_Close IH.
  - move=> n' B IH. exact: CG_Res IH.
  - move=> n' A IHA B _. exact: CG_ParL IHA.
  - move=> n' c r K IH. exact: CG_Ins IH.
  - move=> n' c d K IH. exact: CG_Del IH.
  - move=> n' c b K IH. exact: CG_Sel IH.
  - move=> n' c K1 IH1 K2 IH2. exact: CG_Bra IH1 IH2.
Qed.

Lemma cong_refl n (P : procP n) : cong P P.
Proof. apply: CGC_one. exact: cong1_refl. Qed.

(** ** Symmetry

    The axioms come in symmetric pairs, so every case mirrors a
    constructor; [CG_NuSwap] is self-symmetric via
    [psubst_swap01_invol] (the swapped-swapped body reappears). *)

Lemma cong1_sym n (P Q : procP n) : cong1 P Q -> cong1 Q P.
Proof.
  move=> H; elim: H => {n P Q}.
  - move=> n P Q. exact: CG_PComm.
  - move=> n P Q R. exact: CG_PAssoc'.
  - move=> n P Q R. exact: CG_PAssoc.
  - move=> n P. exact: CG_PZero'.
  - move=> n P. exact: CG_PZero.
  - move=> n. exact: CG_NuZero'.
  - move=> n. exact: CG_NuZero.
  - move=> n P Q. exact: CG_Extr'.
  - move=> n P Q. exact: CG_Extr.
  - move=> n P.
    set Q := psubst (swap_ch zero one) P.
    have -> : P = psubst (swap_ch zero one) Q by rewrite /Q psubst_swap01_invol.
    exact: CG_NuSwap.
  - move=> n. exact: CG_Zero.
  - move=> n P P' Q _ IH. exact: CG_ParL IH.
  - move=> n P P' _ IH. exact: CG_Res IH.
  - move=> n c K K' _ IH. exact: CG_Wait IH.
  - move=> n c K K' _ IH. exact: CG_Close IH.
  - move=> n c r K K' _ IH. exact: CG_Ins IH.
  - move=> n c d K K' _ IH. exact: CG_Del IH.
  - move=> n c b K K' _ IH. exact: CG_Sel IH.
  - move=> n c K1 K1' K2 K2' _ IH1 _ IH2. exact: CG_Bra IH1 IH2.
Qed.

Lemma cong_sym n (P Q : procP n) : cong P Q -> cong Q P.
Proof.
  move=> H; elim: H => {n P Q}.
  - move=> n P Q Hc. exact: CGC_one (cong1_sym Hc).
  - move=> n P Q R _ IH1 _ IH2. exact: CGC_trans IH2 IH1.
Qed.

(** ** Derived congruences for [cong]

    A one-hole context lift: because the scope index of a [cong]
    derivation is generalized by [elim], the frame [f] must be
    reverted so the motive stays well-typed at the generalized
    scope.  Every structural congruence lemma below is an instance,
    with the two-hole [cong_bra] split into two single-hole lifts. *)

Lemma cong_lift m k (f : procP m -> procP k) :
  (forall (P Q : procP m), cong1 P Q -> cong1 (f P) (f Q)) ->
  forall (P Q : procP m), cong P Q -> cong (f P) (f Q).
Proof.
  move=> Hf P Q H.
  move: f Hf; elim: H => {m P Q}.
  - move=> m P Q Hc f Hf. apply: CGC_one. exact: Hf Hc.
  - move=> m P R Q _ IH1 _ IH2 f Hf.
    exact: CGC_trans (IH1 f Hf) (IH2 f Hf).
Qed.

Lemma cong_parL n (P P' Q : procP n) : cong P P' -> cong (P ∥ Q) (P' ∥ Q).
Proof. exact: (cong_lift (f := fun R => R ∥ Q) (fun A B => @CG_ParL n A B Q)). Qed.

Lemma cong_parR n (P Q Q' : procP n) : cong Q Q' -> cong (P ∥ Q) (P ∥ Q').
Proof.
  move=> H.
  have H1 : cong (P ∥ Q) (Q ∥ P) by apply: CGC_one; exact: CG_PComm.
  have H2 : cong (Q ∥ P) (Q' ∥ P) by apply: cong_parL.
  have H3 : cong (Q' ∥ P) (P ∥ Q') by apply: CGC_one; exact: CG_PComm.
  exact: CGC_trans H1 (CGC_trans H2 H3).
Qed.

Lemma cong_par n (P P' Q Q' : procP n) :
  cong P P' -> cong Q Q' -> cong (P ∥ Q) (P' ∥ Q').
Proof.
  move=> HP HQ. exact: CGC_trans (cong_parL Q HP) (cong_parR P' HQ).
Qed.

Lemma cong_nu n (P Q : procP n.+1) : cong P Q -> cong ((ν) P) ((ν) Q).
Proof. exact: (cong_lift (f := @PRes n) (@CG_Res n)). Qed.

Lemma cong_close n (c : pch n) (K K' : procP n) :
  cong K K' -> cong (c !․ K) (c !․ K').
Proof. exact: (cong_lift (f := @PClose n c) (@CG_Close n c)). Qed.

Lemma cong_wait n (c : pch n) (K K' : procP n) :
  cong K K' -> cong (c ?․ K) (c ?․ K').
Proof. exact: (cong_lift (f := @PWait n c) (@CG_Wait n c)). Qed.

Lemma cong_ins n (c : pch n) (r : pol) (K K' : procP n.+1) :
  cong K K' -> cong (c ?( r )․ K) (c ?( r )․ K').
Proof. exact: (cong_lift (f := @PIns n c r) (@CG_Ins n c r)). Qed.

Lemma cong_del n (c d : pch n) (K K' : procP n) :
  cong K K' -> cong (c ! d ․ K) (c ! d ․ K').
Proof. exact: (cong_lift (f := @PDel n c d) (@CG_Del n c d)). Qed.

Lemma cong_sel n (c : pch n) (b : bool) (K K' : procP n) :
  cong K K' -> cong (c ◁ b ․ K) (c ◁ b ․ K').
Proof. exact: (cong_lift (f := @PSel n c b) (@CG_Sel n c b)). Qed.

Lemma cong_bra n (c : pch n) (K1 K1' K2 K2' : procP n) :
  cong K1 K1' -> cong K2 K2' -> cong (c ▷ ( K1 | K2 )) (c ▷ ( K1' | K2' )).
Proof.
  move=> H1 H2.
  apply: (CGC_trans (Q := c ▷ ( K1' | K2 ))).
  - apply: (cong_lift (f := fun K => c ▷ ( K | K2 )) _ H1).
    move=> A B Hc. exact: CG_Bra Hc (cong1_refl K2).
  - apply: (cong_lift (f := fun K => c ▷ ( K1' | K )) _ H2).
    move=> A B Hc. exact: CG_Bra (cong1_refl K1') Hc.
Qed.

(** ** Renaming stability of congruence

    [CG_Extr]/[CG_Extr'] use [psubst_shift_comm] to push [up_ch s]
    past the extruded [shift]; [CG_NuSwap] uses [psubst_swap01_comm];
    everything else is [/=] plus the mirroring constructor. *)

Lemma cong1_ren n (P Q : procP n) (H : cong1 P Q) :
  forall m (s : ren n m), cong1 (psubst s P) (psubst s Q).
Proof.
  elim: H => {n P Q}.
  - move=> n P Q m s /=. exact: CG_PComm.
  - move=> n P Q R m s /=. exact: CG_PAssoc.
  - move=> n P Q R m s /=. exact: CG_PAssoc'.
  - move=> n P m s /=. exact: CG_PZero.
  - move=> n P m s /=. exact: CG_PZero'.
  - move=> n m s /=. exact: CG_NuZero.
  - move=> n m s /=. exact: CG_NuZero'.
  - move=> n P Q m s /=. rewrite psubst_shift_comm. exact: CG_Extr.
  - move=> n P Q m s /=. rewrite psubst_shift_comm. exact: CG_Extr'.
  - move=> n P m s /=. rewrite psubst_swap01_comm. exact: CG_NuSwap.
  - move=> n m s /=. exact: CG_Zero.
  - move=> n P P' Q _ IH m s /=. apply: CG_ParL. exact: (IH m s).
  - move=> n P P' _ IH m s /=. apply: CG_Res. exact: (IH _ (up_ch s)).
  - move=> n c K K' _ IH m s /=. apply: CG_Wait. exact: (IH m s).
  - move=> n c K K' _ IH m s /=. apply: CG_Close. exact: (IH m s).
  - move=> n c r K K' _ IH m s /=. apply: CG_Ins. exact: (IH _ (up_ch s)).
  - move=> n c d K K' _ IH m s /=. apply: CG_Del. exact: (IH m s).
  - move=> n c b K K' _ IH m s /=. apply: CG_Sel. exact: (IH m s).
  - move=> n c K1 K1' K2 K2' _ IH1 _ IH2 m s /=.
    apply: CG_Bra; [exact: (IH1 m s) | exact: (IH2 m s)].
Qed.

Lemma cong_ren n (P Q : procP n) (H : cong P Q) :
  forall m (s : ren n m), cong (psubst s P) (psubst s Q).
Proof.
  elim: H => {n P Q}.
  - move=> n P Q Hc m s. apply: CGC_one. exact: cong1_ren Hc m s.
  - move=> n P Q R _ IH1 _ IH2 m s. exact: CGC_trans (IH1 m s) (IH2 m s).
Qed.

(** ** Forward renaming preservation of the LTS

    Restored from [cemetery/attic/PolAttic.v] (the fundamental
    theorem consumes renaming backwards, so these forward lemmas were
    retired there).  [ltsrP_ren] already lives in [PolEquiv.v] and is
    reused as-is.  Helpers [pren_flip] and [pren_up_zero] come along. *)

Lemma pren_flip m n (s : ren m n) (c : pch m) :
  pren s (pflip c) = pflip (pren s c).
Proof. by case: c. Qed.

Lemma pren_up_zero m n (s : ren m n) (r : pol) :
  pren (up_ch s) ((zero, r) : pch m.+1) = (zero, r).
Proof. by []. Qed.

Lemma ltsfP_ren n (c d : pch n) P P' (H : ltsfP c d P P') :
  forall m (s : ren n m),
    ltsfP (pren s c) (pren s d) (psubst s P) (psubst s P').
Proof.
  elim: H => {n c d P P'}.
  - move=> n c d K m s /=. exact: PF_Pfx.
  - move=> n c d P P' Q _ IH m s /=. exact: PF_ParL (IH _ _).
  - move=> n c d P Q Q' _ IH m s /=. exact: PF_ParR (IH _ _).
  - move=> n c d P P' _ IH m s /=.
    apply: PF_Res. rewrite -!pren_up_pshift. exact: IH.
Qed.

Lemma ltscP_ren n (c : pch n) P P' (H : ltscP c P P') :
  forall m (s : ren n m),
    ltscP (pren s c) (psubst s P) (psubst s P').
Proof.
  elim: H => {n c P P'}.
  - move=> n c K m s /=. exact: PC_Pfx.
  - move=> n c P P' Q _ IH m s /=. exact: PC_ParL (IH _ _).
  - move=> n c P Q Q' _ IH m s /=. exact: PC_ParR (IH _ _).
  - move=> n c P P' _ IH m s /=.
    apply: PC_Res. rewrite -pren_up_pshift. exact: IH.
Qed.

Lemma ltswP_ren n (c : pch n) P P' (H : ltswP c P P') :
  forall m (s : ren n m),
    ltswP (pren s c) (psubst s P) (psubst s P').
Proof.
  elim: H => {n c P P'}.
  - move=> n c K m s /=. exact: PW_Pfx.
  - move=> n c P P' Q _ IH m s /=. exact: PW_ParL (IH _ _).
  - move=> n c P Q Q' _ IH m s /=. exact: PW_ParR (IH _ _).
  - move=> n c P P' _ IH m s /=.
    apply: PW_Res. rewrite -pren_up_pshift. exact: IH.
Qed.

Lemma ltsbP_ren n (c : pch n) (r : pol) P (P' : procP n.+1)
    (H : ltsbP c r P P') :
  forall m (s : ren n m),
    ltsbP (pren s c) r (psubst s P) (psubst (up_ch s) P').
Proof.
  elim: H => {n c r P P'}.
  - move=> n c r P P' Hf m s /=.
    have HF := ltsfP_ren Hf (up_ch s).
    rewrite pren_up_pshift pren_up_zero in HF.
    exact: PB_Open HF.
  - move=> n c r P P' Q _ IH m s /=.
    rewrite psubst_shift_comm. exact: PB_ParL (IH _ _).
  - move=> n c r P Q Q' _ IH m s /=.
    rewrite psubst_shift_comm. exact: PB_ParR (IH _ _).
  - move=> n c r P P' _ IH m s /=.
    rewrite psubst_swap01_comm.
    apply: PB_Res. rewrite -pren_up_pshift. exact: IH.
Qed.

Lemma ltsselP_ren n (c : pch n) b P P' (H : ltsselP c b P P') :
  forall m (s : ren n m),
    ltsselP (pren s c) b (psubst s P) (psubst s P').
Proof.
  elim: H => {n c b P P'}.
  - move=> n c b K m s /=. exact: PS_Pfx.
  - move=> n c b P P' Q _ IH m s /=. exact: PS_ParL (IH _ _).
  - move=> n c b P Q Q' _ IH m s /=. exact: PS_ParR (IH _ _).
  - move=> n c b P P' _ IH m s /=.
    apply: PS_Res. rewrite -pren_up_pshift. exact: IH.
Qed.

Lemma ltsbrP_ren n (c : pch n) b P P' (H : ltsbrP c b P P') :
  forall m (s : ren n m),
    ltsbrP (pren s c) b (psubst s P) (psubst s P').
Proof.
  elim: H => {n c b P P'}.
  - move=> n c b K1 K2 m s /=.
    have := PBR_Pfx (pren s c) b (psubst s K1) (psubst s K2).
    by case: b.
  - move=> n c b P P' Q _ IH m s /=. exact: PBR_ParL (IH _ _).
  - move=> n c b P Q Q' _ IH m s /=. exact: PBR_ParR (IH _ _).
  - move=> n c b P P' _ IH m s /=.
    apply: PBR_Res. rewrite -pren_up_pshift. exact: IH.
Qed.

Lemma ltstP_ren n (P P' : procP n) (H : ltstP P P') :
  forall m (s : ren n m), ltstP (psubst s P) (psubst s P').
Proof.
  elim: H => {n P P'}.
  - move=> n P P' Q _ IH m s /=. exact: PT_ParL (IH _ _).
  - move=> n P Q Q' _ IH m s /=. exact: PT_ParR (IH _ _).
  - move=> n P P' _ IH m s /=. exact: PT_Res (IH _ _).
  - move=> n c P P' Q Q' Hc Hw m s /=.
    have HW := ltswP_ren Hw s. rewrite pren_flip in HW.
    exact: PT_CW (ltscP_ren Hc s) HW.
  - move=> n c P P' Q Q' Hw Hc m s /=.
    have HW := ltswP_ren Hw s. rewrite pren_flip in HW.
    exact: PT_WC HW (ltscP_ren Hc s).
  - move=> n c d P P' Q Q' Hf Hr m s /=.
    have HR := ltsrP_ren Hr s. rewrite pren_flip in HR.
    exact: PT_DR (ltsfP_ren Hf s) HR.
  - move=> n c d P P' Q Q' Hr Hf m s /=.
    have HR := ltsrP_ren Hr s. rewrite pren_flip in HR.
    exact: PT_RD HR (ltsfP_ren Hf s).
  - move=> n c r P P' Q Q' Hb Hr m s /=.
    have HR := ltsrP_ren Hr (up_ch s).
    rewrite pren_up_pshift pren_flip pren_up_zero psubst_shift_comm in HR.
    exact: PT_BR (ltsbP_ren Hb s) HR.
  - move=> n c r P P' Q Q' Hr Hb m s /=.
    have HR := ltsrP_ren Hr (up_ch s).
    rewrite pren_up_pshift pren_flip pren_up_zero psubst_shift_comm in HR.
    exact: PT_RB HR (ltsbP_ren Hb s).
  - move=> n c b P P' Q Q' Hs Hbr m s /=.
    have HB := ltsbrP_ren Hbr s. rewrite pren_flip in HB.
    exact: PT_SB (ltsselP_ren Hs s) HB.
  - move=> n c b P P' Q Q' Hbr Hs m s /=.
    have HB := ltsbrP_ren Hbr s. rewrite pren_flip in HB.
    exact: PT_BS HB (ltsselP_ren Hs s).
Qed.

Lemma ltstsP_ren n (P P' : procP n) (H : P —τ*→ P') :
  forall m (s : ren n m), psubst s P —τ*→ psubst s P'.
Proof.
  elim: H => [n0 X|n0 X Y R Hst _ IH] m s.
  - exact: PTS_refl.
  - exact: PTS_step (ltstP_ren Hst s) (IH _ _).
Qed.

