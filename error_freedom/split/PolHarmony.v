(** ** Backward τ-inversion under full injectivity

    Resurrected from [cemetery/attic/PolAttic.v].  A τ-step of
    [psubst s P] for an INJECTIVE [s] descends to a τ-step of [P];
    the two co-subjects share a preimage because full injectivity
    lets [pren_inj] cancel [pren s].  Applied below at [s := shift]
    and [s := up_ch shift] to close the [CG_Extr']/bound-send frames.
    [pren_inj] is the only private helper the proof needs that is not
    already in scope; [psubst_inv_par]/[psubst_inv_res] and the
    [lts*P_ren_inv] suite live in [PolEquiv.v]. *)

Lemma pren_inj m n (s : ren m n) :
  (forall z1 z2, s z1 = s z2 -> z1 = z2) ->
  forall c d : pch m, pren s c = pren s d -> c = d.
Proof.
  move=> Hinj [c1 c2] [d1 d2] /= [E1 E2].
  by rewrite (Hinj _ _ E1) E2.
Qed.

Lemma ltstP_ren_inv n' X R (H : ltstP X R) :
  forall m (s : ren m n') (P : procP m),
    (forall z1 z2, s z1 = s z2 -> z1 = z2) ->
    psubst s P = X ->
    exists P0, R = psubst s P0 /\ ltstP P P0.
Proof.
  elim: H => {n' X R}.
  - move=> n' A A' B _ IH m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]]. rewrite EB.
    case: (IH _ _ _ Hinj (esym EA)) => P0 [-> H0].
    exists (P0 ∥ B0). split=> //=. exact: PT_ParL H0.
  - move=> n' A B B' _ IH m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]]. rewrite EA.
    case: (IH _ _ _ Hinj (esym EB)) => P0 [-> H0].
    exists (A0 ∥ P0). split=> //=. exact: PT_ParR H0.
  - move=> n' B B' _ IH m s P Hinj EP.
    case: (psubst_inv_res EP) => B0 [-> EB].
    have Hinj' : forall z1 z2 : ch m.+1, up_ch s z1 = up_ch s z2 -> z1 = z2.
      move=> [z1|] [z2|] //= E.
      by rewrite (Hinj z1 z2
        (f_equal (fun c : ch n'.+1 => if c is Some u then u else s z1) E)).
    case: (IH _ _ _ Hinj' (esym EB)) => P0 [-> H0].
    exists ((ν) P0). split=> //=. exact: PT_Res H0.
  - move=> n' C A A' B B' Hc Hw m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltscP_ren_inv Hc (esym EA)) => c [PA [E1 -> HA]].
    case: (ltswP_ren_inv Hw (esym EB)) => w [PB [E2 -> HB]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (PA ∥ PB). split=> //=. exact: PT_CW HA HB.
  - move=> n' C A A' B B' Hw Hc m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltswP_ren_inv Hw (esym EA)) => w [PA [E2 -> HA]].
    case: (ltscP_ren_inv Hc (esym EB)) => c [PB [E1 -> HB]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (PA ∥ PB). split=> //=. exact: PT_WC HA HB.
  - move=> n' C D A A' B B' Hf Hr m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsfP_ren_inv Hf (esym EA)) => c [d [PA [E1 ED -> HA]]].
    case: (ltsrP_ren_inv Hr (esym EB)) => w [PB [E2 ER Hu]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (PA ∥ psubst (scons d.1 id_ren) PB). split=> /=.
    + congr (_ ∥ _). rewrite ER ED !psubst_comp.
      by apply: psubst_ext => -[z|].
    + apply: PT_DR HA _.
      have Ed : D.2 = d.2 by rewrite ED.
      have Hu' := Hu d.1. rewrite Ed -surjective_pairing in Hu'.
      exact: Hu'.
  - move=> n' C D A A' B B' Hr Hf m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsrP_ren_inv Hr (esym EA)) => w [PA [E2 ER Hu]].
    case: (ltsfP_ren_inv Hf (esym EB)) => c [d [PB [E1 ED -> HB]]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (psubst (scons d.1 id_ren) PA ∥ PB). split=> /=.
    + congr (_ ∥ _). rewrite ER ED !psubst_comp.
      by apply: psubst_ext => -[z|].
    + apply: PT_RD _ HB.
      have Ed : D.2 = d.2 by rewrite ED.
      have Hu' := Hu d.1. rewrite Ed -surjective_pairing in Hu'.
      exact: Hu'.
  - move=> n' C r A A' B B' Hb Hr m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsbP_ren_inv Hb (esym EA)) => c [PA [E1 -> HA]].
    have EBs : psubst shift B = psubst (up_ch s) (psubst shift B0).
      by rewrite EB psubst_shift_comm.
    move: Hr. rewrite EBs => Hr.
    case: (ltsrP_ren_inv Hr erefl) => w [PB [E2 ER Hu]].
    have Hinj' : forall z1 z2 : ch m.+1, up_ch s z1 = up_ch s z2 -> z1 = z2.
      move=> [z1|] [z2|] //= E.
      by rewrite (Hinj z1 z2
        (f_equal (fun cc : ch n'.+1 => if cc is Some u then u else s z1) E)).
    have E2' : pren (up_ch s) w = pren (up_ch s) (pshift (pflip c)).
      by rewrite -E2 E1 pren_up_pshift pren_flip.
    have Ew := pren_inj Hinj' E2'. subst w.
    exists ((ν) (PA ∥ psubst (scons zero id_ren) PB)). split=> /=.
    + congr PRes. congr (_ ∥ _). rewrite ER /= !psubst_comp.
      by apply: psubst_ext => -[z|].
    + apply: PT_BR HA _.
      exact: (Hu zero).
  - move=> n' C r A A' B B' Hr Hb m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsbP_ren_inv Hb (esym EB)) => c [PB [E1 -> HB]].
    have EAs : psubst shift A = psubst (up_ch s) (psubst shift A0).
      by rewrite EA psubst_shift_comm.
    move: Hr. rewrite EAs => Hr.
    case: (ltsrP_ren_inv Hr erefl) => w [PA [E2 ER Hu]].
    have Hinj' : forall z1 z2 : ch m.+1, up_ch s z1 = up_ch s z2 -> z1 = z2.
      move=> [z1|] [z2|] //= E.
      by rewrite (Hinj z1 z2
        (f_equal (fun cc : ch n'.+1 => if cc is Some u then u else s z1) E)).
    have E2' : pren (up_ch s) w = pren (up_ch s) (pshift (pflip c)).
      by rewrite -E2 E1 pren_up_pshift pren_flip.
    have Ew := pren_inj Hinj' E2'. subst w.
    exists ((ν) (psubst (scons zero id_ren) PA ∥ PB)). split=> /=.
    + congr PRes. congr (_ ∥ _). rewrite ER /= !psubst_comp.
      by apply: psubst_ext => -[z|].
    + apply: PT_RB _ HB.
      exact: (Hu zero).
  - move=> n' C b A A' B B' Hs Hbr m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsselP_ren_inv Hs (esym EA)) => c [PA [E1 -> HA]].
    case: (ltsbrP_ren_inv Hbr (esym EB)) => w [PB [E2 -> HB]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (PA ∥ PB). split=> //=. exact: PT_SB HA HB.
  - move=> n' C b A A' B B' Hbr Hs m s P Hinj EP.
    case: (psubst_inv_par EP) => A0 [B0 [-> EA EB]].
    case: (ltsbrP_ren_inv Hbr (esym EA)) => w [PA [E2 -> HA]].
    case: (ltsselP_ren_inv Hs (esym EB)) => c [PB [E1 -> HB]].
    have E2' : pren s w = pren s (pflip c) by rewrite -E2 E1 pren_flip.
    have Ew := pren_inj Hinj E2'. subst w.
    exists (PA ∥ PB). split=> //=. exact: PT_BS HA HB.
Qed.

(** Injectivity of the two renamings we actually invert τ under. *)
Lemma shift_name_inj n : forall z1 z2 : ch n, shift z1 = shift z2 -> z1 = z2.
Proof. by move=> z1 z2 [->]. Qed.

Lemma up_ch_name_inj m n (s : ren m n) :
  (forall z1 z2, s z1 = s z2 -> z1 = z2) ->
  forall z1 z2 : ch m.+1, up_ch s z1 = up_ch s z2 -> z1 = z2.
Proof.
  move=> Hinj [z1|] [z2|] //= E.
  by rewrite (Hinj z1 z2 (shift_name_inj E)).
Qed.

Lemma up_shift_name_inj n :
  forall z1 z2 : ch n.+1, up_ch shift z1 = up_ch shift z2 -> z1 = z2.
Proof. exact: up_ch_name_inj (@shift_name_inj n). Qed.

(** ** τ-transition inversions at ν and ∥ *)

Lemma pinv_t_nuend n (R : procP n) : ltstP ((ν) ∅) R -> False.
Proof.
  move=> H. pinv H.
  match goal with [ K : ltstP ∅ _ |- _ ] => exact: (pinv_t_end K) end.
Qed.

Lemma ltstP_resE n (P : procP n.+1) R :
  ltstP ((ν) P) R -> exists P', R = (ν) P' /\ ltstP P P'.
Proof. move=> H. pinv H. eexists; (split; [reflexivity|eassumption]). Qed.

Lemma ltstP_parE n (A B : procP n) T :
  ltstP (A ∥ B) T ->
  (exists A', T = A' ∥ B /\ ltstP A A') \/
  (exists B', T = A ∥ B' /\ ltstP B B') \/
  (exists (c : pch n) A' B', [/\ T = A' ∥ B', ltscP c A A' & ltswP (pflip c) B B']) \/
  (exists (c : pch n) A' B', [/\ T = A' ∥ B', ltswP (pflip c) A A' & ltscP c B B']) \/
  (exists (c d : pch n) A' B', [/\ T = A' ∥ B', ltsfP c d A A' & ltsrP (pflip c) d B B']) \/
  (exists (c d : pch n) A' B', [/\ T = A' ∥ B', ltsrP (pflip c) d A A' & ltsfP c d B B']) \/
  (exists (c : pch n) (r : pol) A' B',
      [/\ T = (ν) (A' ∥ B'), ltsbP c r A A' & ltsrP (pshift (pflip c)) (zero, r) (⟨shift⟩ B) B']) \/
  (exists (c : pch n) (r : pol) A' B',
      [/\ T = (ν) (A' ∥ B'), ltsrP (pshift (pflip c)) (zero, r) (⟨shift⟩ A) A' & ltsbP c r B B']) \/
  (exists (c : pch n) (b : bool) A' B', [/\ T = A' ∥ B', ltsselP c b A A' & ltsbrP (pflip c) b B B']) \/
  (exists (c : pch n) (b : bool) A' B', [/\ T = A' ∥ B', ltsbrP (pflip c) b A A' & ltsselP c b B B']).
Proof.
  move=> H. pinv H.
  - left. eexists. split; [reflexivity|eassumption].
  - right; left. eexists. split; [reflexivity|eassumption].
  - do 2 right; left. do 3 eexists. split; [reflexivity|eassumption|eassumption].
  - do 3 right; left. do 3 eexists. split; [reflexivity|eassumption|eassumption].
  - do 4 right; left. do 4 eexists. split; [reflexivity|eassumption|eassumption].
  - do 5 right; left. do 4 eexists. split; [reflexivity|eassumption|eassumption].
  - do 6 right; left. do 4 eexists. split; [reflexivity|eassumption|eassumption].
  - do 7 right; left. do 4 eexists. split; [reflexivity|eassumption|eassumption].
  - do 8 right; left. do 4 eexists. split; [reflexivity|eassumption|eassumption].
  - do 9 right. do 4 eexists. split; [reflexivity|eassumption|eassumption].
Qed.

(** ** τ bisimulation: transition-building and residual helpers *)

(* Every LTS constructor as a hint: [eauto with htr] re-derives the
   frame of a transition on a reassociated term from the two
   sub-transitions in context (psubst distributes over ∥ by whnf, so
   the [_ParL]/[_ParR] frames fire under a shifted idle component). *)
Hint Constructors ltstP ltscP ltswP ltsfP ltsrP ltsbP ltsselP ltsbrP : htr.

(* [psubst] distributes over ∥ definitionally; naming it lets keyed
   unification see the [∥] head under a bound-send receiver frame. *)
Lemma psubst_shift_par n (Q R : procP n) :
  psubst shift (Q ∥ R) = (psubst shift Q ∥ psubst shift R).
Proof. by []. Qed.

Lemma psubst_shift_res n (P : procP n.+1) :
  psubst shift ((ν) P) = (ν) (psubst (up_ch shift) P).
Proof. by []. Qed.

Lemma pren_swap_pshift_zero n (r : pol) :
  pren (swap_ch zero one) (pshift ((zero, r) : pch n.+1)) = ((zero, r) : pch n.+2).
Proof. by []. Qed.

Lemma pren_shift_pshift n (c : pch n) : pren shift c = pshift c.
Proof. by []. Qed.

Lemma pren_swap_zero n (r : pol) :
  pren (swap_ch zero one) ((zero, r) : pch n.+2) = ((one, r) : pch n.+2).
Proof. by []. Qed.

(* Scope-extrusion + associativity, the two ν-residual shapes that
   appear whenever a BOUND send crosses a monoid axiom. *)
Lemma cong_extr_assoc n (X Y : procP n.+1) (Z : procP n) :
  (((ν) (X ∥ Y)) ∥ Z) ≡ ((ν) (X ∥ (Y ∥ ⟨shift⟩ Z))).
Proof.
  apply: (CGC_trans (Q := (ν) ((X ∥ Y) ∥ ⟨shift⟩ Z))).
  - apply: CGC_one. exact: CG_Extr.
  - apply: cong_nu. apply: CGC_one. exact: CG_PAssoc.
Qed.

Lemma cong_assoc_extrB n (Z : procP n) (X Y : procP n.+1) :
  ((ν) ((⟨shift⟩ Z ∥ X) ∥ Y)) ≡ (Z ∥ (ν) (X ∥ Y)).
Proof.
  apply: (CGC_trans (Q := (ν) ((X ∥ Y) ∥ ⟨shift⟩ Z))).
  - apply: cong_nu. exact: cong_exR.
  - apply: (CGC_trans (Q := ((ν) (X ∥ Y)) ∥ Z)).
    + apply: cong_sym. apply: CGC_one. exact: CG_Extr.
    + apply: CGC_one. exact: CG_PComm.
Qed.

(* Subject re-association for the CG_Extr' backward frame: a visible
   action of [⟨shift⟩Q] has a [pren shift] subject, so the co-subject
   [c] on the [P] side is [pren shift (pflip c0)] of the preimage. *)
Lemma extr'_flip n (c : pch n.+1) (c0 : pch n) :
  pflip c = pren shift c0 -> c = pren shift (pflip c0).
Proof.
  move=> E. have H := f_equal pflip E. by rewrite pflip_invol -pren_flip in H.
Qed.

(* τ within [P∥Q] transported across the right associativity frame:
   reassociate to [P∥(Q∥R)] with an [R] idle on the right. *)
Lemma ltst_pushR n (P Q R : procP n) A' :
  ltstP (P ∥ Q) A' -> exists X, ltstP (P ∥ (Q ∥ R)) X /\ (A' ∥ R) ≡ X.
